  function inflate($bytes) {
    global $ZLIB_DICT;
    $context = inflate_init(ZLIB_ENCODING_RAW, [ 'dictionary' => $ZLIB_DICT ] );
    return inflate_add($context, $bytes, ZLIB_FINISH);
  }


